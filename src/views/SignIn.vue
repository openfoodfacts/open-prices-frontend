<template>
  <v-alert
    class="mb-2"
    type="info"
    variant="outlined"
  >Use your Open Food Facts credentials to sign in.</v-alert>
  <v-form @submit.prevent="signIn">
    <v-text-field
      v-model="signinForm.username"
      label="Username"
      type="text"
      hint="Open Food Facts username"
    ></v-text-field>
    <v-text-field
      v-model="signinForm.password"
      label="Password"
      type="password"
      hint="Open Food Facts password"
    ></v-text-field>
    <v-btn
      type="submit"
      class="mt-2"
      :loading="loading"
      :disabled="!formFilled"
    >Sign in</v-btn>
  </v-form>
</template>

<script>
import { useCookies } from "@vueuse/integrations/useCookies";
import api from "../services/api";

export default {
  data() {
    return {
      signinForm: {
        username: "",
        password: "",
      },
      loading: false,
    };
  },
  computed: {
    formFilled() {
      return Object.values(this.signinForm).every(x => !!x)
    }
  },
  methods: {
    signIn() {
      this.loading = true
      api
        .signIn(this.signinForm.username, this.signinForm.password)
        .then((data) => {
          if (data["access_token"]) {
            useCookies().set("username", this.signinForm.username)
            useCookies().set("access_token", data["access_token"])
            this.$router.push("/add")
          } else {
            alert("Error: wrong credentials")
            this.loading = false
          }
        })
        .catch((error) => {
          alert("Error: server error")
          this.loading = false
        });
    },
  },
};
</script>
